def make_shirt(size='large', message='I love Python'):

    print(f"\nI'm going to make a {size}-sized shirt with the message '{message}' printed on it.")

# I make a large shirt with the message
make_shirt()

# Make a medium shirt with the message
make_shirt(size='medium')

# Make a shirt of any size with a different message
make_shirt('small', 'Code. Sleep. Repeat.')
