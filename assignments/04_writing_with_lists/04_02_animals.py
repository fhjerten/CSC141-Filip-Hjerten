# I Make a list with at least three animals that share a characteristic

animals = ["dog", "cat", "rabbit"]

# I Use a for loop to print different sentences about each animal

for animal in animals:
    if animal == "dog":
        print("A dog would be a loyal companion.")
    elif animal == "cat":
        print("A cat would be an independent but loving pet.")
    elif animal == "rabbit":
        print("A rabbit would be a quiet and gentle pet.")

# I add a line outside of the loop to write something common about all the animals.

print("\nAny of these animals would make a great pet!")